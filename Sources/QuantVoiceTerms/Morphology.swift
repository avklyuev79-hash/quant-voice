//
//  Morphology.swift
//  QuantVoiceTerms
//
//  Отсечение русских окончаний перед фонетическим сопоставлением.
//
//  Зачем. Матчер сравнивал ослышку с термином целиком, и это ломалось на
//  первом же падеже: «клоут» ловился, «Клоутка» — нет, потому что лишний
//  слог уводил фонетический ключ за порог. Лечить это добавлением вариантов
//  в словарь бесполезно: «Клоуда», «Клоудом», «Клоутками», «в Кванте» —
//  список бесконечен, а каждый вариант ещё и стоит места в промпте.
//
//  Что делаем. Русская речь про латинские бренды склоняет их по обычным
//  правилам, поэтому от слова отрезается известное окончание (а заодно
//  уменьшительный суффикс «-к-»), и с термином сравнивается основа.
//  Полноценной морфологии здесь нет и не нужно: словарь основ русского мы
//  не тянем, задача узкая — снять флексию с заимствованного слова.
//
//  Почему это безопасно. Кандидатов порождается несколько, и совпадение
//  по усечённому кандидату матчер принимает по более жёсткому порогу,
//  чем совпадение по слову целиком (см. TermMatcher). Плюс остаётся
//  внешний фильтр: слово, которое система знает орфографически, фонетика
//  не трогает вообще. Ложная замена портит правильный текст и потому
//  строго хуже пропуска (ТЗ 6.6).
//

import Foundation

public enum Morphology {

    /// Основы-кандидаты для сопоставления, от самой длинной к самой короткой.
    /// Первый элемент — всегда само слово: точное совпадение должно выигрывать
    /// у любого усечения.
    ///
    /// `truncated` у кандидата означает «это результат отсечения», и матчер
    /// применяет к такому кандидату более строгий порог.
    public static func stemCandidates(for word: String) -> [Candidate] {
        let lower = word.lowercased()
        var out: [Candidate] = [Candidate(text: lower, truncated: false)]
        var seen: Set<String> = [lower]

        func add(_ stem: String) {
            guard isAcceptableStem(stem, original: lower) else { return }
            guard seen.insert(stem).inserted else { return }
            out.append(Candidate(text: stem, truncated: true))
        }

        for ending in endings where lower.hasSuffix(ending) {
            let stem = String(lower.dropLast(ending.count))
            add(stem)
            // Второй шаг: уменьшительный суффикс «-к-» под окончанием.
            // «клоутками» → «клоутк» → «клоут», «клоутка» → «клоутк» → «клоут».
            if stem.hasSuffix("к") {
                add(String(stem.dropLast()))
            }
        }

        return out
    }

    public struct Candidate: Equatable, Sendable {
        public let text: String
        /// Получен отсечением окончания, а не взят как есть.
        public let truncated: Bool

        public init(text: String, truncated: Bool) {
            self.text = text
            self.truncated = truncated
        }
    }

    // MARK: - Правила

    /// Основа должна остаться узнаваемой: слишком короткий или слишком сильно
    /// обрезанный остаток годится под что угодно и порождает ложные замены.
    /// 3 символа — тот же нижний предел, что и у фонетики; 55% длины не даёт
    /// съесть больше, чем окончание («квантование» не превратится в «квант»).
    private static func isAcceptableStem(_ stem: String, original: String) -> Bool {
        guard stem.count >= 3 else { return false }
        return Double(stem.count) >= Double(original.count) * 0.55
    }

    /// Окончания существительных и прилагательных плюс уменьшительные формы.
    /// Порядок значения не имеет — перебираются все подходящие, кандидатов
    /// на слово получается единицы.
    ///
    /// Чего здесь сознательно нет: глагольных окончаний. Термины — это
    /// названия и существительные, а «-ать/-ить/-ешь» открыли бы дорогу
    /// к сопоставлению с обычными глаголами.
    private static let endings: [String] = [
        // Четыре символа — суффикс с окончанием.
        "ками", "ках", "кам", "ками",
        // Три символа.
        "ами", "ями", "ому", "ему", "ыми", "ими", "ах", "ях",
        // Два символа.
        "ом", "ем", "ам", "ям", "ов", "ев", "ей", "ой", "ий", "ый",
        "ая", "ое", "ые", "ых", "ым", "им", "ку", "ке", "ка", "ки", "ко",
        // Один символ. «о» тоже окончание («Квонто»), «ь» и «й» — часть
        // склонения заимствований.
        "а", "я", "у", "ю", "е", "и", "ы", "о", "й", "ь",
    ]
}
