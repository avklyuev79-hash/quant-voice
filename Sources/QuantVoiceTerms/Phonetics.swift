//
//  Phonetics.swift
//  QuantVoiceTerms
//
//  Фонетическое пространство для сопоставления кириллических ослышек
//  с латинскими терминами: «клоут коворг» ↔ «Claude Cowork».
//
//  Почему не готовый Soundex/Metaphone: они построены вокруг АНГЛИЙСКОЙ
//  орфографии (немые буквы, gh/kn/wr) и работают внутри одного алфавита.
//  Наша задача другая: два разных письма нужно сначала привести к общему
//  звуковому виду, и «слышит» здесь русское ухо — Whisper с русским языком
//  записывает английское так, как его слышит русскоговорящий. Поэтому свой,
//  нарочно маленький алгоритм:
//
//  1. Слово переводится в «фонетический ключ» — строку классов звуков.
//     Классы объединяют то, что русский слух (и модель) не различает:
//     звонкие/глухие пары (д/т, г/к, б/п, з/с — оглушение на конце:
//     «коворг»/«коворк»), безударные гласные (аканье о→а, е/э/и/ы→и),
//     шипящие одной кучей (ж/ш/щ/ч), в/ф/w.
//  2. Латиница отображается по приближённым правилам английского чтения:
//     th→т, wh→в, ph→ф, oo→у, ee/ea→и, немое конечное e («Claude»→клауд).
//     Это грубо — английская орфография нерегулярна («Chrome» читается
//     через «к», «church» — нет), но для брендов и терминов срабатывает
//     в большинстве случаев, а промахи закрываются явными вариантами
//     произношения в словаре — они дают фонетике честные кириллические
//     опоры вместо угаданного чтения.
//  3. Сравнение — Левенштейн с ВЕСАМИ ПО ПОЗИЦИИ и порогом от длины ключа.
//     Порог консервативный: ложная замена портит правильный текст
//     и потому строго хуже пропуска (ТЗ 6.6).
//
//  Про веса. Расхождение в начале слова и расхождение в хвосте значат
//  разное. Ослышка почти всегда сохраняет начало («клоут» / «клоуда» /
//  «клоутка» — все начинаются на «кло»), а плывут у неё окончание и
//  безударный хвост. Симметричный Левенштейн этого не знал: он одинаково
//  прощал «клоут»→«флоут» (другое слово) и «клоут»→«клоуд» (та же ослышка
//  в другом падеже). Поэтому первый звук стоит вдвое, остальные — как были.
//
//  Проверка на живых примерах (ключи совпадают или в пределах порога):
//  Claude=KLAUT ↔ клоут=KLAUT, клод=KLAT(1); Cowork=KAFARK ↔ коворг=KAFARK;
//  Anthropic=ANTRAPIK ↔ антропик=ANTRAPIK, энтропик=INTRAPIK(2, первый звук);
//  WhisperKit=FISPIRKIT ↔ висперкит=FISPIRKIT; Quant=KUANT ↔ квант=KFANT(1).
//

import Foundation

public enum Phonetics {

    // MARK: - Ключ

    /// Фонетический ключ слова. Пустая строка — слово не дало ни одного
    /// значимого звука (цифры, пунктуация).
    public static func key(for word: String) -> String {
        var s = word.lowercased()

        // Немое конечное «e» английских слов: Claude → klaud, Chrome → chrom.
        // Только для чисто ASCII-слов — в русских конечное «е» звучит.
        if s.count >= 3, s.last == "e", s.allSatisfy(\.isASCII),
           let beforeLast = s.dropLast().last, !"aeiouy".contains(beforeLast) {
            s = String(s.dropLast())
        }

        // Диграфы — до посимвольной таблицы, пока контекст ещё виден.
        for (from, to) in Self.digraphs {
            s = s.replacingOccurrences(of: from, with: to)
        }

        var result = ""
        for ch in s {
            // Не буква (или ь/ъ) — выпадает: в таблице её нет.
            guard let mapped = Self.classMap[ch] else { continue }
            // Схлопываем повторы: «нн» и «н» звучат одинаково.
            for symbol in mapped where result.last != symbol {
                result.append(symbol)
            }
        }
        return result
    }

    // MARK: - Сравнение

    /// Совпадают ли ключи с учётом допуска. Допуск считается от ключа ТЕРМИНА:
    /// термин — известная величина, а длина ослышки может гулять.
    ///
    /// `requireSameOnset` включается для основ, полученных отсечением окончания
    /// (см. Morphology): усечение и так расширило пространство совпадений,
    /// и совпадение первого звука — дешёвая страховка от того, чтобы оно
    /// разъехалось совсем.
    public static func withinTolerance(termKey: String,
                                       wordKey: String,
                                       requireSameOnset: Bool = false) -> Bool {
        guard !termKey.isEmpty, !wordKey.isEmpty else { return false }
        if requireSameOnset, termKey.first != wordKey.first { return false }

        let tolerance = maxDistance(forKeyLength: termKey.count)
        guard tolerance > 0 else { return termKey == wordKey }
        // Дешёвая отсечка до полного Левенштейна: даже при весе 1 на символ
        // разница длин уже стоит столько же правок.
        guard abs(termKey.count - wordKey.count) <= tolerance else { return false }
        return distance(termKey, wordKey) <= tolerance
    }

    /// Порог по длине ключа ТЕРМИНА, в единицах веса.
    ///
    /// Числа консервативные сознательно: на коротких ключах одна правка —
    /// это уже другое слово («кот»/«код»), на длинных пара расхождений тонет
    /// в остальных совпавших звуках. Верхняя ступень (3) появилась вместе
    /// с весами: у длинного термина хвост может разъехаться сильно, но голова
    /// теперь защищена отдельно, и разрешать хвосту больше стало безопасно.
    public static func maxDistance(forKeyLength length: Int) -> Int {
        switch length {
        case ..<4:    return 0
        case 4...6:   return 1
        case 7...10:  return 2
        default:      return 3
        }
    }

    /// Вес расхождения на позиции ключа термина (1-based).
    /// Первый звук — вдвое: ослышка его сохраняет почти всегда.
    static func weight(atTermPosition position: Int) -> Int {
        position <= 1 ? 2 : 1
    }

    /// Левенштейн со стоимостью правки, зависящей от позиции в ключе термина.
    /// Память O(min), ключи короткие (< 20 символов) — скорость здесь не вопрос.
    ///
    /// Стоимость берётся по позиции в ПЕРВОМ аргументе (ключ термина): это
    /// известная величина, а слово может быть какой угодно длины, и мерить
    /// «начало» по нему было бы неустойчиво.
    public static func distance(_ termKey: String, _ wordKey: String) -> Int {
        let x = Array(termKey), y = Array(wordKey)
        if x.isEmpty { return y.count }
        if y.isEmpty { return x.reduce(into: 0) { sum, _ in sum += 1 } + weight(atTermPosition: 1) - 1 }

        // Строка 0: слово целиком вставлено, термин не начат — вставки
        // на позиции 1 стоят по весу первого звука.
        var previous = [Int](repeating: 0, count: y.count + 1)
        for j in 1...y.count { previous[j] = previous[j - 1] + weight(atTermPosition: 1) }
        var current = [Int](repeating: 0, count: y.count + 1)

        for i in 1...x.count {
            let w = weight(atTermPosition: i)
            current[0] = previous[0] + w
            for j in 1...y.count {
                let cost = x[i - 1] == y[j - 1] ? 0 : w
                current[j] = min(previous[j] + w,          // удаление звука термина
                                 current[j - 1] + w,       // лишний звук в слове
                                 previous[j - 1] + cost)   // замена
            }
            swap(&previous, &current)
        }
        return previous[y.count]
    }

    // MARK: - Таблицы

    /// Диграфы обоих алфавитов. Порядок важен: «sh» должен уйти в шипящую
    /// раньше, чем «h» посчитается отдельным звуком. «ch»→к — выбор в пользу
    /// технических брендов (Chrome, Chat*); слова с «ч»-чтением закрываются
    /// вариантами в словаре. «ce/ci/cy» — мягкое чтение латинской c.
    private static let digraphs: [(String, String)] = [
        ("wh", "в"), ("th", "т"), ("ph", "ф"), ("sh", "ш"), ("ch", "к"), ("ck", "к"),
        ("oo", "у"), ("ee", "и"), ("ea", "и"),
        ("ce", "се"), ("ci", "си"), ("cy", "си"),
        ("дж", "ж"),
    ]

    /// Буква → класс звука. Классы: гласные A/I/U; согласные P T K F S H M N L R
    /// и W — шипящие. ь и ъ отсутствуют в таблице и выпадают.
    private static let classMap: [Character: String] = [
        // Латиница (после диграфов).
        "a": "A", "b": "P", "c": "K", "d": "T", "e": "I", "f": "F", "g": "K",
        "h": "H", "i": "I", "j": "W", "k": "K", "l": "L", "m": "M", "n": "N",
        "o": "A", "p": "P", "q": "K", "r": "R", "s": "S", "t": "T", "u": "U",
        "v": "F", "w": "F", "x": "KS", "y": "I", "z": "S",
        // Кириллица.
        "а": "A", "б": "P", "в": "F", "г": "K", "д": "T", "е": "I", "ё": "A",
        "ж": "W", "з": "S", "и": "I", "й": "I", "к": "K", "л": "L", "м": "M",
        "н": "N", "о": "A", "п": "P", "р": "R", "с": "S", "т": "T", "у": "U",
        "ф": "F", "х": "H", "ц": "S", "ч": "W", "ш": "W", "щ": "W", "ы": "I",
        "э": "I", "ю": "U", "я": "A",
    ]
}
